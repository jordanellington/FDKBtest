// ── Bedrock client ──────────────────────────────────────────────────────────

const bedrock = new BedrockRuntimeClient({
  region: process.env.AWS_BEDROCK_REGION || 'us-east-1',
  credentials: {
    accessKeyId: process.env.AWS_BEDROCK_ACCESS_KEY_ID,
    secretAccessKey: process.env.AWS_BEDROCK_SECRET_ACCESS_KEY,
  },
});

const MODEL_ID = process.env.AWS_BEDROCK_MODEL_ID || 'us.anthropic.claude-haiku-4-5-20251001-v1:0';

// ── Alfresco fetch ──────────────────────────────────────────────────────────

const ALFRESCO_COOKIE = process.env.ALFRESCO_COOKIE;

if (!ALFRESCO_COOKIE) {
  console.error('ERROR: Set ALFRESCO_COOKIE env var with your active session cookie.');
  console.error('Example: ALFRESCO_COOKIE="JSESSIONID=ABC123; Alfresco-CSRFToken=XYZ" node scripts/extract-ccc-metadata.js');
  process.exit(1);
}

async function fetchPdf(nodeId) {
  const url = `${ALFRESCO_API}/alfresco/versions/1/nodes/${nodeId}/content`;
  const resp = await fetch(url, {
    headers: { 'Cookie': ALFRESCO_COOKIE },
  });
  if (!resp.ok) throw new Error(`Failed to fetch ${nodeId}: ${resp.status}`);
  return Buffer.from(await resp.arrayBuffer());
}

// ── Haiku single-pass extraction + CCC match ────────────────────────────────

async function extractAndClassify(pdfBuffer, docName) {
  const base64Pdf = pdfBuffer.toString('base64');

  const command = new InvokeModelCommand({
    modelId: MODEL_ID,
    contentType: 'application/json',
    accept: 'application/json',
    body: JSON.stringify({
      anthropic_version: 'bedrock-2023-05-31',
      max_tokens: 1024,
      messages: [{
        role: 'user',
        content: [
          {
            type: 'document',
            source: {
              type: 'base64',
              media_type: 'application/pdf',
              data: base64Pdf,
            },
          },
          {
            type: 'text',
            text: EXTRACTION_PROMPT,
          },
        ],
      }],
    }),
  });

  const response = await bedrock.send(command);
  const body = JSON.parse(new TextDecoder().decode(response.body));
  const text = body.content?.[0]?.text || '';

  try {
    const cleaned = text.replace(/^```(?:json)?\s*\n?/i, '').replace(/\n?```\s*$/i, '').trim();
    return JSON.parse(cleaned);
  } catch {
    console.warn(`  ⚠ Failed to parse JSON for ${docName}, raw response:`, text.slice(0, 300));
    return null;
  }
}

// ── Main ────────────────────────────────────────────────────────────────────

async function main() {
  const outputPath = join(__dirname, '../data/ccc-pilot-results.json');
  mkdirSync(dirname(outputPath), { recursive: true });

  // Resume support: load existing results and skip already-processed docs
  let results = [];
  const alreadyProcessed = new Set();
  if (existsSync(outputPath)) {
    try {
      results = JSON.parse(readFileSync(outputPath, 'utf8'));
      for (const r of results) alreadyProcessed.add(r.nodeId);
      console.log(`📂 Resuming — ${alreadyProcessed.size} documents already processed\n`);
    } catch { /* start fresh */ }
  }

  const remaining = TARGET_DOCS.filter(d => !alreadyProcessed.has(d.id));
  const total = TARGET_DOCS.length;
  const startIdx = total - remaining.length;

  console.log('╔══════════════════════════════════════════════════════════════╗');
  console.log(`║  CCC Single-Pass Extraction — ${total} documents${' '.repeat(Math.max(0, 18 - String(total).length))}║`);
  console.log('╚══════════════════════════════════════════════════════════════╝\n');

  if (remaining.length === 0) {
    console.log('✅ All documents already processed. Delete the output file to re-run.\n');
  }

  let errorCount = 0;
  let processed = 0;
  const startTime = Date.now();
  const CONCURRENCY = parseInt(process.env.CCC_CONCURRENCY || '5', 10);

  async function processDoc(doc, idx) {
    const label = `[${idx}/${total}] 📄 ${doc.name}`;
    try {
      const pdfBuffer = await fetchPdf(doc.id);
      const result = await extractAndClassify(pdfBuffer, doc.name);
      if (!result) {
        console.log(`${label} ❌ extraction failed`);
        return { nodeId: doc.id, name: doc.name, error: 'extraction_failed' };
      }
      const badge = {
        'Internal Only': '🟡',
        'Internal + Client (with notice)': '🟠',
        'External Unrestricted': '🟢',
        'Not Covered': '🔴',
      }[result.cccDistroLevel] || '⚪';
      console.log(`${label} ${badge} ${result.cccDistroLevel}`);
      return { nodeId: doc.id, name: doc.name, ...result };
    } catch (err) {
      console.log(`${label} ❌ ${err.message}`);
      return { nodeId: doc.id, name: doc.name, error: err.message };
    }
  }

  // Process in batches of CONCURRENCY
  for (let i = 0; i < remaining.length; i += CONCURRENCY) {
    const batch = remaining.slice(i, i + CONCURRENCY);
    const batchResults = await Promise.all(
      batch.map((doc, j) => processDoc(doc, startIdx + i + j + 1))
    );
    for (const r of batchResults) {
      results.push(r);
      if (r.error) errorCount++;
    }
    processed += batch.length;

    // Save after every batch
    writeFileSync(outputPath, JSON.stringify(results, null, 2));

    // Progress
    const elapsed = (Date.now() - startTime) / 1000;
    const rate = (processed / (elapsed / 60)).toFixed(0);
    console.log(`  ── batch done: ${results.length}/${total} total, ${rate} docs/min ──`);
  }

  const elapsed = ((Date.now() - startTime) / 1000).toFixed(0);
  if (remaining.length > 0) {
    console.log(`\n⏱  Processed ${remaining.length} docs in ${elapsed}s (${(remaining.length / (elapsed / 60)).toFixed(0)} docs/min)`);
    if (errorCount > 0) console.log(`⚠  ${errorCount} errors`);
  }

  // ── Summary ─────────────────────────────────────────────────────────────
  console.log('\n══════════════════════════════════════════════════════════════');
  console.log('RESULTS SUMMARY\n');

  for (const r of results) {
    if (r.error) {
      console.log(`  ❌ ${r.name}: ERROR — ${r.error}`);
      continue;
    }

    const badge = {
      'Internal Only': '🟡',
      'Internal + Client (with notice)': '🟠',
      'External Unrestricted': '🟢',
      'Not Covered': '🔴',
    }[r.cccDistroLevel] || '⚪';

    console.log(`  ${badge} ${r.name}`);
    console.log(`     Article:     ${r.articleTitle || '—'}`);
    console.log(`     Publication: ${r.publicationTitle || '—'}`);
    console.log(`     Publisher:   ${r.publisher || '—'}`);
    console.log(`     ISSN:        ${r.issn || '—'}`);
    console.log(`     Authors:     ${r.authors || '—'}`);
    console.log(`     Date:        ${r.publicationDate || '—'}`);
    console.log(`     Copyright:   ${r.copyrightHolder || '—'}`);
    console.log(`     CCC Level:   ${r.cccDistroLevel}`);
    console.log(`     Matched On:  ${r.cccMatchedOn}`);
    console.log(`     Confidence:  ${r.confidence || '—'}`);
    console.log();
  }

  // ── Final save ─────────────────────────────────────────────────────────
  writeFileSync(outputPath, JSON.stringify(results, null, 2));
  console.log(`📁 Full results written to: ${outputPath}`);

  // ── Stats ─────────────────────────────────────────────────────────────
  const levels = results.reduce((acc, r) => {
    const l = r.cccDistroLevel || 'Error';
    acc[l] = (acc[l] || 0) + 1;
    return acc;
  }, {});
  console.log('\n📊 Distribution breakdown:');
  for (const [level, count] of Object.entries(levels)) {
    console.log(`   ${level}: ${count}`);
  }
}

main().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
